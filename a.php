<!-- item 15 -->

<p>Jadilah diri sendiri dengan menjalani apa yang paling kamu sukai, lalu perbanyak konten Instagram dengan apa yang kamu sukai tersebut. Konten Instagram ini tak boleh asal banyak saja, namun harus dibarengi oleh keunikan serta apa yang akan dilihat orang sebagai hal yang 'beda' dari akun lain.</p>

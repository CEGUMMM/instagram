<p>Dengan membiarkan akun Instagram terbuka akan membuat unggahan mendapat banyak like dan komentar dari pengguna lain. Mereka bisa memberikan like pada fotomu walaupun nggak mengikuti akun. Dengan begini, memungkinkan unggahan masuk ke fitur explore, sehingga orang lain akan melihat dan tertarik untuk mengikuti akun milikmu.</p>
